package com.monami.inquiry.dao;

public class InquiryDTO {
	private int iqr_idx;
	private String iqr_catagory;
	private String iqr_writer;
	private String iqr_email;
	private String iqr_phone;
	private String iqr_title;
	private String iqr_content;
	public int getIqr_idx() {
		return iqr_idx;
	}
	public void setIqr_idx(int iqr_idx) {
		this.iqr_idx = iqr_idx;
	}
	public String getIqr_catagory() {
		return iqr_catagory;
	}
	public void setIqr_catagory(String iqr_catagory) {
		this.iqr_catagory = iqr_catagory;
	}
	public String getIqr_writer() {
		return iqr_writer;
	}
	public void setIqr_writer(String iqr_writer) {
		this.iqr_writer = iqr_writer;
	}
	public String getIqr_email() {
		return iqr_email;
	}
	public void setIqr_email(String iqr_email) {
		this.iqr_email = iqr_email;
	}
	public String getIqr_phone() {
		return iqr_phone;
	}
	public void setIqr_phone(String iqr_phone) {
		this.iqr_phone = iqr_phone;
	}
	public String getIqr_title() {
		return iqr_title;
	}
	public void setIqr_title(String iqr_title) {
		this.iqr_title = iqr_title;
	}
	public String getIqr_content() {
		return iqr_content;
	}
	public void setIqr_content(String iqr_content) {
		this.iqr_content = iqr_content;
	}
	
}
